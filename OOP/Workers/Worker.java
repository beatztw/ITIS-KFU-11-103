package Workers;

public class Worker {
        String name;
        String profession;
        int age;
        double weight;
        double height;

        public Worker(String name, String profession, int age , double weight, double height){
            this.name = name;
            this.profession = profession;
            this.age = age;
            this.weight = weight;
            this.height = height;
        }
    }

